<h2 id="down">Prevention</h2></font><font color="Beige" size="4">
<h3>Protect yourself and others around you by knowing the facts and taking appropriate precautions. Follow advice provided by your local health authority.</h3>
<hr>
<font size="4" color="Cornsilk">
<h3>To prevent the spread of COVID-19:</h3>
<ul id="pre" type="disc"><center>
<li>Cover your nose and mouth with your bent elbow or a tissue when you cough or sneeze.</li>
<li>If you have a fever, cough and difficulty breathing, seek medical attention.</li>
<li>Don’t touch your eyes, nose or mouth.</li>
<li>Maintain a safe distance from anyone who is coughing or sneezing.</li>
<li>Stay home if you feel unwell.</li>
<li>Clean your hands often. Use soap and water, or an alcohol-based hand rub.</li>
<li>Wear a mask when physical distancing is not possible.</li>
</ul></center>
Calling in advance allows your healthcare provider to quickly direct you to the right health facility. This protects you, and prevents the spread of viruses and other infections.
Masks
Masks can help prevent the spread of the virus from the person wearing the mask to others.
Masks alone do not protect against COVID-19, and should be combined with physical distancing and hand hygiene.
Follow the advice provided by your local health authority.<p>
If COVID-19 is spreading in your community, stay safe by taking some simple precautions, such as physical distancing, wearing a mask,
keeping rooms well ventilated, avoiding crowds, cleaning your hands, and coughing into a bent elbow or tissue.
Check local advice where you live and work.
Outbreaks have been reported in restaurants, choir practices, fitness classes, nightclubs,
offices and places of worship where people have gathered,</p><p>
often in crowded indoor settings where they talk loudly, shout, breathe heavily or sing.
The risks of getting COVID-19 are higher in crowded and inadequately ventilated spaces where infected people spend long periods of time together in close proximity.
These environments are where the virus appears to spread by respiratory droplets or aerosols more efficiently, so taking precautions is even more important.
</p></font>

